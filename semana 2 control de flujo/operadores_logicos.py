#####################################
# Operadores Lógicos y Relacionales #
#####################################

a = 5
b = 10

print(a > b)   # False
print(a < b)   # True
print(a == b)  # False
print(a != b)  # True

# Operadores lógicos
print(a < b and b > 5)  # True
print(a > b or b > 5)   # True
print(not (a > b))      # True
